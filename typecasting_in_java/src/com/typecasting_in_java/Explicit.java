package com.typecasting_in_java;

public class Explicit {
	public static void main(String[] args) {
		// byte, short, int, long, float, double

		int var1 = 30;
		System.out.println("Int type: " + var1);

		byte var2 = (byte) var1;
		System.out.println("Byte type: " + var2);

		double var3 = 30;
		System.out.println("Double type: " + var3);

		short var4 = (short) var3;
		System.out.println("Short type: " + var4);

	}
}
